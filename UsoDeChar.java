public class UsoDeChar {
    public static void main(String[] args) {
        char letra1 = 'a';
        char letra2 = 'v';
        char letra3 = 'i';
        char letra4 = 'รณ';
        char letra5 = 'n';

        System.out.println("Letra 1: " + letra1);
        System.out.println("Letra 3: " + letra3);
        System.out.println("Todas juntas: " + letra1 + letra2 + letra3 + letra4 + letra5);
        System.out.println("palabra: "+letra2 + letra3+letra5);
    }
}
